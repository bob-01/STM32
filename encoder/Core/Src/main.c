/* USER CODE BEGIN 4 */
uint8_t I2C_scanner(I2C_HandleTypeDef *hi2c) {
/**
 * This tests the standard 7-bit addresses
 * Devices with higher bit address might not be seen properly.
 * The i2c_scanner uses the return value of
 * the Write.endTransmisstion to see if
 * a device did acknowledge to the address.
*/
	clrScr();
	setColor(64, 255, 64);
	setBackColor(0,0,0);
	print("I2C Scanner", CENTER, 1, 0);

	uint8_t error, address;
	int nDevices;

	setColor(64, 64, 64);
	print("Scanning...", 1, cfont.y_size*1, 0);

	nDevices = 0;
	position_x = 80;

	for(address = 15; address < 127; address++ ) {
	// The i2c_scanner uses the return value of
	// the Write.endTransmisstion to see if
	// a device did acknowledge to the address.

	    error = HAL_I2C_IsDeviceReady(hi2c, (uint16_t) address << 1 , 10, 100);

		setColor(255, 255, 64);

		if(error == HAL_OK)
		{
			print("found address:", cfont.x_size*0, cfont.y_size*(6+nDevices), 0);
			printNumI(address , cfont.x_size*16, cfont.y_size*(6+nDevices), 1,' ');
			position_x += cfont.y_size;
			nDevices++;
			continue;
		}

		if(error == HAL_ERROR)
		{
			printNumI(address , 0, cfont.y_size*2, 1,' ');
			print("HAL_ERROR", cfont.x_size*4, cfont.y_size*2, 0);
			continue;
		}
		if(error == HAL_BUSY)
		{
			printNumI(address , 1, 30, 1,' ');
			print("HAL_BUSY", cfont.x_size*16, cfont.y_size*3, 0);
			continue;
		}
		if(error == HAL_TIMEOUT)
		{
			printNumI(address , 1, 30, 1,' ');
			print("HAL_TIMEOUT", cfont.x_size*0, cfont.y_size*4, 0);
			continue;
		}
		HAL_Delay(500);
	}

	if (nDevices == 0){
		setColor(255, 64, 64);
		print("No I2C devices found", cfont.x_size*0, cfont.y_size*5, 0);
	}else{
		setColor(255, 0, 0);
		print("Address found:", cfont.x_size*0, cfont.y_size*5, 0);
		printNumI(nDevices , cfont.x_size*16, cfont.y_size*5, 1,' ');
	}
	HAL_Delay(5000);
	clrScr();
	position_x = 0;
	return nDevices;
}
